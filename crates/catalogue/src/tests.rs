fn write_pack(
    path: &Path,
    manifest: &ContentPackManifest,
    actual_asset: &[u8],
    extras: &[(&str, &[u8])],
    asset_compression: CompressionMethod,
) {
    let file = File::create(path).unwrap();
    let mut writer = ZipWriter::new(file);
    let stored = SimpleFileOptions::default().compression_method(CompressionMethod::Stored);
    let compressed = SimpleFileOptions::default().compression_method(asset_compression);
    writer.start_file(MANIFEST_PATH, stored).unwrap();
    writer
        .write_all(&canonical_manifest_bytes(manifest).unwrap())
        .unwrap();
    writer
        .start_file(&manifest.items[0].variants[0].asset.path, compressed)
        .unwrap();
    writer.write_all(actual_asset).unwrap();
    for &(name, bytes) in extras {
        writer.start_file(name, stored).unwrap();
        writer.write_all(bytes).unwrap();
    }
    writer.finish().unwrap();
}

