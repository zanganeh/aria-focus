#[test]
fn generated_local_verification_uses_its_own_trust_contract() {
    let temp = tempfile::tempdir().unwrap();
    let job_id = "job-local-catalogue";
    let asset = b"local generated flac fixture";
    let mut manifest = fixture_manifest(asset);
    manifest.pack.id = format!("generated.local.{job_id}");
    manifest.pack.version = "1.0.0".to_owned();
    manifest.pack.app_version_requirement = "*".to_owned();
    manifest.items[0].id = format!("generated.local.{job_id}.item");
    manifest.items[0].human_qa = HumanQa {
        status: HumanQaStatus::Draft,
        reviews: vec![],
    };
    manifest.items[0].variants[0].asset = AudioAsset {
        path: format!("assets/generated/{job_id}.flac"),
        sha256: hash_bytes(asset),
        bytes: asset.len() as u64,
        codec: AssetCodec::Flac,
        sample_rate_hz: 48_000,
        channels: 2,
        bit_depth: Some(24),
    };
    manifest.validate_generated_local(job_id).unwrap();
    assert!(manifest.validate_published().is_err());

    let directory = temp.path().join("generated");
    fs::create_dir_all(directory.join("assets/generated")).unwrap();
    let canonical = canonical_manifest_bytes(&manifest).unwrap();
    fs::write(directory.join(MANIFEST_PATH), &canonical).unwrap();
    fs::write(
        directory.join(format!("assets/generated/{job_id}.flac")),
        asset,
    )
    .unwrap();

    assert!(verify_generated_local_pack(&directory, &hash_bytes(&canonical)).is_ok());
    assert!(verify_installed_pack(&directory, &hash_bytes(&canonical)).is_err());
}

