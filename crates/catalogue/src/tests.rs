fn published_ogg_opus_requires_its_exact_playback_contract() {
    let bytes = asset_bytes();
    let mut manifest = fixture_manifest(&bytes);
    manifest.format_version = FORMAT_VERSION_2;
    let asset = &mut manifest.items[0].variants[0].asset;
    asset.path = "assets/test-item.opus".to_owned();
    asset.codec = AssetCodec::OggOpus;
    asset.sample_rate_hz = 48_000;
    asset.channels = 2;
    asset.bit_depth = None;
    manifest.validate_published().unwrap();

    let mut v1_opus = manifest.clone();
    v1_opus.format_version = FORMAT_VERSION;
    assert!(v1_opus.validate_published().is_err());

    let mut v2_flac = fixture_manifest(&bytes);
    v2_flac.format_version = FORMAT_VERSION_2;
    assert!(v2_flac.validate_published().is_err());

    let mutations: [fn(&mut AudioAsset); 3] = [
        |asset: &mut AudioAsset| asset.sample_rate_hz = 44_100,
        |asset: &mut AudioAsset| asset.channels = 3,
        |asset: &mut AudioAsset| asset.bit_depth = Some(16),
    ];
    for mutate in mutations {
        let mut invalid = manifest.clone();
        mutate(&mut invalid.items[0].variants[0].asset);
        assert!(invalid.validate_published().is_err());
    }
}

