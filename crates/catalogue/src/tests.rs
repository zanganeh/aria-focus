fn single_loop_manifest(
    bytes: &[u8],
    pack_id: &str,
    item_id: &str,
    duration_seconds: f32,
    deep_work_suitability: f32,
) -> ContentPackManifest {
    let mut manifest = fixture_manifest(bytes);
    manifest.pack.id = pack_id.to_owned();
    manifest.pack.title = format!("{pack_id} title");
    manifest.items[0].id = item_id.to_owned();
    manifest.items[0].title = item_id.to_owned();
    manifest.items[0].variants[0].asset.path = format!("assets/{item_id}.wav");
    manifest.items[0].analysis.duration_seconds = duration_seconds;
    manifest.items[0]
        .activity_suitability
        .iter_mut()
        .find(|entry| entry.activity == Activity::DeepWork)
        .unwrap()
        .suitability = deep_work_suitability;
    manifest
}

fn estimated_samples(candidate: &crate::PlaybackCandidate) -> f64 {
    f64::from(candidate.duration_seconds)
        * f64::from(candidate.asset.sample_rate_hz)
        * f64::from(candidate.asset.channels)
}

